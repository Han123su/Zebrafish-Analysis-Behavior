% handles    structure with handles and user data (see GUIDATA)

% -------------Cropping Images-------------
videoFileName = get(handles.edit1);
ze_reader = vision.VideoFileReader(videoFileName.String);
videoPlayer = vision.VideoPlayer('Position',[20,400,700,400]);
i=0;
while ~isDone(ze_reader)
    frame = step(ze_reader);
    if i==0
        imshow(frame,'Parent',handles.axes1);  
        [xv,yv] = ginput(2);
        y1_plus = xv(1); 
        y2_plus = xv(2); 
        x1_plus = yv(1);
        x2_plus = yv(2);  
    end
    frame =  frame(yv(1):yv(2),xv(1):xv(2),:);
    step(videoPlayer,frame)
    i =i+1;
    imwrite(frame,strcat('./CROP_IMG/',num2str(handles.total),'.png'));
end
