function [col] = color_set()
% 定義32種顏色的RGB值
colors = [
    255, 0, 0;     % 紅色
    255, 128, 0;   % 橙色
    255, 255, 0;   % 黃色
    0, 255, 0;     % 綠色
    0, 0, 255;     % 藍色
    128, 0, 255;   % 紫色
    255, 0, 255;   % 粉紅色
    0, 255, 255;   % 青色
    128, 128, 200; % 灰色
    255, 0, 128;   % 玫瑰色
    128, 0, 0;     % 深紅色
    128, 128, 0;   % 深黃色
    0, 128, 0;     % 深綠色
    0, 128, 128;   % 深青色
    0, 0, 128;     % 深藍色
    192, 192, 192; % 銀色
    255, 192, 203; % 粉紅色
    255, 255, 224; % 淺黃色
    144, 238, 144; % 淺綠色
    175, 238, 238; % 淺青色
    135, 206, 250; % 淺藍色
    221, 160, 221; % 李子色
    255, 165, 0;   % 橙黃色
    184, 134, 11;  % 金色
    255, 255, 0;   % 亮黃色
    0, 255, 0;     % 亮綠色
    0, 255, 255;   % 亮青色
    70, 130, 180;  % 鋼藍色
    139, 69, 19;   % 巧克力色
    210, 105, 30;  % 巧克力色
    244, 164, 96;  % 淺木色
    139, 0, 139;   % 紫羅蘭色
    128, 0, 128;   % 紫色
    0, 0, 0        % 黑色
];

% 正規化顏色值到[0, 1]範圍
colors = colors / 255;

% 創建色彩映射
col = colormap(colors);
end